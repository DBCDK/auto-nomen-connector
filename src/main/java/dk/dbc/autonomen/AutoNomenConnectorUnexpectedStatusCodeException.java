package dk.dbc.autonomen;

public class AutoNomenConnectorUnexpectedStatusCodeException extends AutoNomenConnectorException{

    private final int statusCode;

    public AutoNomenConnectorUnexpectedStatusCodeException(String message, int statusCode) {
        super(message);
        this.statusCode = statusCode;
    }

    public int getStatusCode() {
        return statusCode;
    }
}
